
package programa;


public class calculadora {
    double resultado;
    public double suma (double x , double y){
    
    
    resultado = x + y;   
    return resultado;
}
    public double resta (double x , double y ){
    
    resultado = x-y;
    return resultado;
    
    }
    public double multi (double x , double y){
        resultado = x * y;
        return resultado;
    }
    public double division (double x , double y){
        resultado = x * y;
        return resultado;
    }
}